#!/usr/bin/env node
// Reads all LC + drift-rule JSON from the linked name-place-faction-generator packages
// and writes a single bundled TS file so Vite can include it in the app bundle
// without needing import.meta.glob to traverse symlinked node_modules.
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const genRoot = resolve(root, '../name-place-faction-generator');

if (!existsSync(genRoot)) {
  console.error(`[bundle-npf-data] ERROR: generator repo not found at ${genRoot}`);
  process.exit(1);
}

function readJson(p) {
  return JSON.parse(readFileSync(p, 'utf8'));
}

const lcIndex = readJson(resolve(genRoot, 'shared/data/lc-index.json'));
const lcDistance = readJson(resolve(genRoot, 'shared/data/lc-distance.json'));
const placeDescriptors = readJson(resolve(genRoot, 'packages/placegen/data/place-descriptors.json'));

const namegenLcs = {};
const namegenLcDir = resolve(genRoot, 'packages/namegen/data/lc');
for (const f of readdirSync(namegenLcDir).filter(f => f.endsWith('.json'))) {
  namegenLcs[f.replace('.json', '')] = readJson(resolve(namegenLcDir, f));
}

const driftRules = {};
const driftDir = resolve(genRoot, 'packages/namegen/data/drift-rules');
for (const f of readdirSync(driftDir).filter(f => f.endsWith('.json'))) {
  driftRules[f.replace('.json', '')] = readJson(resolve(driftDir, f));
}

const out = `// AUTO-GENERATED by scripts/bundle-npf-data.mjs — do not edit
/* eslint-disable */
// @ts-nocheck
export const NPF_LC_INDEX = ${JSON.stringify(lcIndex)};
export const NPF_LC_DISTANCE = ${JSON.stringify(lcDistance)};
export const NPF_PLACE_DESCRIPTORS = ${JSON.stringify(placeDescriptors)};
export const NPF_NAMEGEN_LCS: Record<string, unknown> = ${JSON.stringify(namegenLcs)};
export const NPF_DRIFT_RULES: Record<string, unknown> = ${JSON.stringify(driftRules)};
`;

const outPath = resolve(root, 'src/lib/npf-data.generated.ts');
writeFileSync(outPath, out);
console.log(`[bundle-npf-data] Written ${outPath} (${(Buffer.byteLength(out) / 1024).toFixed(0)} KB)`);
